export const getShape = (id) => {
	const shape = SHAPES[id];
	if (!shape) return null;
	// Default to polygon type if not specified (for backward compatibility)
	if (!shape.type) {
		return { ...shape, type: 'polygon' };
	}
	return JSON.parse(JSON.stringify(shape));
};
